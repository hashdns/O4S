// Accounts
var account;

web3.eth.getAccounts(function(err, accounts) {
  if (err != null) {
    alert("GetAccounts error, try again...verify metamask.");
    return;
  }
  if (accounts.length == 0) {
    alert("Nenhuma conta encontrada! Verifique se o Ethereum client est√° configurado corretamente.");
    return;
  }
  account = accounts[0];
  console.log('Account: ' + account);
  web3.eth.defaultAccount = account;
  document.getElementById("account").innerHTML = account;
});


//Smart contract functions

async function tokenURI(id) {
	
    const uri = await contract.methods.tokenURI(id).call();	
	return uri;
			
}


async function tokenId(){
	
	const token_id = await contract.methods.nextTokenId().call();	
	console.log("Token Id: ", token_id);
	return token_id;
	
} 

